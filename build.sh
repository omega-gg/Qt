#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

Qt_versionA="5.12.3"
Qt_versionB="5.12"

Qt="http://download.qt.io/official_releases/qt/$Qt_versionB/$Qt_versionA/single/qt-everywhere-src-$Qt_versionA.tar.xz"

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

if [ $# != 1 ] || [ $1 != "win32" -a $1 != "win64" -a $1 != "linux" -a $1 != "macOS" ]; then

    echo "Usage: build <win32 | win64 | linux | macOS>"

    exit 1
fi

#--------------------------------------------------------------------------------------------------
# Download
#--------------------------------------------------------------------------------------------------

echo ""
echo "DOWNLOADING Qt"
echo $Qt
curl -L -o Qt.tar.gz $Qt

#--------------------------------------------------------------------------------------------------
# Qt
#--------------------------------------------------------------------------------------------------

tar -xf Qt.tar.gz

#--------------------------------------------------------------------------------------------------
# Deploy
#--------------------------------------------------------------------------------------------------

mkdir deploy
